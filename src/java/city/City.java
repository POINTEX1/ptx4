package city;

/**
 *
 * @author patricio alberto
 */
public class City {
    
    public int idCity;
    public String nameCity;

    public City() {
    }

    public int getIdCity() {
        return idCity;
    }

    public void setIdCity(int idCity) {
        this.idCity = idCity;
    }

    public String getNameCity() {
        return nameCity;
    }

    public void setNameCity(String nameCity) {
        this.nameCity = nameCity;
    }
    
}
